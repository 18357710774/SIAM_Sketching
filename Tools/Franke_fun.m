function y = Franke_fun(x)
x1 = x(:,1);
x2 = x(:,2);
x3 = x(:,3);

term1 = exp(-(9.*x1 - 2).^2/4 - (9.*x2 - 2).^2/4 - (9.*x3 - 2).^2/4);
term2 = exp(-(9.*x1 + 1).^2/49 - (9.*x2 + 1)./10 - (9.*x3 + 1)./10);
term3 = exp(-(9.*x1 - 7).^2/4 - (9.*x2 - 3).^2/4 - (9.*x3 - 5).^2/4);
term4 = exp(-(9.*x1 - 4).^2 - (9.*x2 - 7).^2 - (9.*x3 - 5).^2);

y = 0.75 * term1 + 0.75 * term2 + 0.5 * term3 - 0.2 * term4;
